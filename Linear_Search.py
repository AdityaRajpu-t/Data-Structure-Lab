arr = []
print("Enter 10 numbers:")
for i in range(10):
    num = int(input())
    arr.append(num)
num_to_search = int(input("Enter number to be searched: "))
index = -1
for i in range(10):
    if arr[i] == num_to_search:
        index = i
        break
if index != -1:
    print("Number found at index:", index)
else:
    print("Number not found")
